"""
WealthPath AI — Multi-Agent Workflow
-------------------------------------
7 specialized agents orchestrated sequentially:
  JUDGE (Perceive): Profile → Spending → Cashflow
  ACT   (Act):      Goal → Product → Report
  VERIFY (Verify):  RiskGuard

Each agent accepts structured input and returns a typed dict (Structured Output).
In production, LLM calls use JSON Schema / Structured Output to prevent hallucination.
Numeric calculations are handled by TypeScript-equivalent Python Tool functions —
the LLM only generates natural-language explanations, never raw numbers.
"""

from __future__ import annotations
from dataclasses import dataclass, field
from typing import Any
import json


# ─── Shared data models ───────────────────────────────────────────────────────

@dataclass
class UserProfile:
    age: int
    monthly_income: int          # KRW
    current_assets: int          # KRW
    monthly_fixed_expense: int   # KRW
    goals: list[str]
    retirement_age: int = 63
    expected_pension: int = 0    # KRW / month


@dataclass
class Transaction:
    category: str
    amount: int   # KRW


# ─── Tool functions (numeric computation — NOT generated by LLM) ─────────────

def _calc_months_to_goal(target: int, current: int, monthly_saving: int) -> int:
    """Calculate months needed to reach target amount."""
    if monthly_saving <= 0:
        return 999
    gap = max(target - current, 0)
    return max(1, round(gap / monthly_saving))


def _calc_retirement_cashflow(
    monthly_pension: int,
    financial_assets: int,
    monthly_living: int,
    monthly_medical: int,
    drawdown_years: int = 25,
) -> dict:
    """
    Estimate post-retirement monthly cashflow.
    Asset drawdown assumes equal monthly distribution over drawdown_years.
    """
    asset_monthly = financial_assets // (drawdown_years * 12)
    monthly_income = monthly_pension + asset_monthly
    monthly_expense = monthly_living + monthly_medical
    shortage = monthly_income - monthly_expense
    return {
        "monthly_income": monthly_income,
        "monthly_expense": monthly_expense,
        "monthly_shortage": shortage,                  # negative = deficit
        "30yr_cumulative_shortage": abs(min(shortage, 0)) * 12 * 30,
        "risk_level": "위험" if shortage < -600000 else
                      "주의" if shortage < 0 else "양호",
    }


def _physical_equivalent(amount_krw: int) -> str:
    """Map a savings amount to a relatable real-world equivalent."""
    if amount_krw >= 30_000_000:
        return "중형 SUV 1대"
    if amount_krw >= 10_000_000:
        return "비상금 목표 달성"
    if amount_krw >= 4_500_000:
        return "월세 3개월치"
    if amount_krw >= 2_280_000:
        return "국내 여행 숙박비 (제주 2박)"
    if amount_krw >= 1_000_000:
        return "최신 스마트폰 구입"
    return f"커피 {amount_krw // 5000}잔"


# ─── Agent definitions ────────────────────────────────────────────────────────

class ProfileAgent:
    """
    JUDGE — Perceive
    Classifies the user's life stage and financial priority based on their profile.
    Output is a Structured JSON used as input by downstream agents.
    """

    LIFE_STAGES = {
        "seed":        (19, 30),
        "asset_build": (30, 45),
        "retirement_prep": (45, 60),
        "retirement":  (60, 99),
    }

    def run(self, profile: UserProfile) -> dict:
        stage = next(
            (k for k, (lo, hi) in self.LIFE_STAGES.items()
             if lo <= profile.age < hi),
            "seed",
        )
        risk = (
            "high"   if profile.age < 35 else
            "medium" if profile.age < 50 else
            "low"
        )
        priority = (
            "비상금 확보 → 고정저축률 설정" if stage == "seed" else
            "자산형성 + 연금 시작"        if stage == "asset_build" else
            "은퇴 현금흐름 점검"           if stage == "retirement_prep" else
            "현금흐름 관리 + 금융보호"
        )
        return {
            "agent": "ProfileAgent",
            "life_stage": stage,
            "risk_capacity": risk,
            "priority": priority,
            "mode": "early_career" if stage in ("seed", "asset_build") else "retirement",
        }


class SpendingAgent:
    """
    JUDGE — Perceive
    Categorises transactions and flags over-spending items.
    """

    THRESHOLDS = {
        "배달": 0.10,   # >10% of income → warning
        "카페": 0.05,
        "구독": 0.03,
        "쇼핑": 0.15,
    }

    def run(self, transactions: list[Transaction], monthly_income: int) -> dict:
        category_totals: dict[str, int] = {}
        for tx in transactions:
            category_totals[tx.category] = category_totals.get(tx.category, 0) + tx.amount

        total = sum(category_totals.values())
        warnings = [
            cat for cat, amt in category_totals.items()
            if self.THRESHOLDS.get(cat, 1.0) < amt / max(monthly_income, 1)
        ]

        return {
            "agent": "SpendingAgent",
            "monthly_spending": total,
            "category_breakdown": category_totals,
            "overspend_warnings": warnings,
            "savings_capacity": max(monthly_income - total, 0),
        }


class CashflowAgent:
    """
    JUDGE — Perceive
    Calculates retirement cashflow and identifies monthly deficit.
    Delegates all numeric computation to _calc_retirement_cashflow() Tool.
    """

    def run(self, profile: UserProfile, monthly_medical: int = 250_000) -> dict:
        result = _calc_retirement_cashflow(
            monthly_pension=profile.expected_pension,
            financial_assets=profile.current_assets,
            monthly_living=profile.monthly_fixed_expense,
            monthly_medical=monthly_medical,
        )
        return {"agent": "CashflowAgent", **result}


class GoalAgent:
    """
    ACT — Act
    Generates Range-type saving nudges and calculates goal timeline scenarios.
    Uses _calc_months_to_goal() and _physical_equivalent() Tools — not LLM maths.
    """

    def run(
        self,
        profile: UserProfile,
        spending: dict,
        target_amount: int = 10_000_000,
    ) -> dict:
        base_saving = spending["savings_capacity"]

        scenarios = {}
        for label, extra in [
            ("현재 속도 (절약 없음)", 0),
            ("배달 주 1회↓ (월 +4만)", 40_000),
            ("배달 주 2회↓ (월 +8만)", 80_000),
            ("배달 2회 + 카페·구독 정리 (월 +15만)", 150_000),
        ]:
            months = _calc_months_to_goal(
                target_amount, profile.current_assets, base_saving + extra
            )
            scenarios[label] = months

        best_months = min(scenarios.values())
        six_month_savings = (base_saving + 40_000) * 6
        physical = _physical_equivalent(six_month_savings)

        return {
            "agent": "GoalAgent",
            "goal_amount": target_amount,
            "scenarios": scenarios,
            "best_case_months": best_months,
            "range_nudge": "배달 주문을 주 1~2회 줄이면 월 4~8만원 절약 가능",
            "physical_equivalent_6m": physical,
        }


