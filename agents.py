class RiskGuardAgent:
    """
    VERIFY — Verify
    Scans all agent outputs for compliance violations before rendering.
    Acts as a Compliance-aware Guardrail layer.
    """

    FORBIDDEN_PHRASES = [
        "수익률 보장", "원금 보장", "확실히 오릅니다", "반드시",
        "guaranteed return", "certain profit",
    ]

    def run(self, all_results: list[dict], explanation_text: str = "") -> dict:
        violations = []

        # Check for individual stock recommendations
        for r in all_results:
            if r.get("agent") == "ProductAgent":
                for p in r.get("products", []):
                    if any(kw in p["name"] for kw in ["삼성", "카카오", "KOSPI", "ETF 직접"]):
                        violations.append(f"개별 종목 추천 감지: {p['name']}")

        # Check explanation text for forbidden phrases
        for phrase in self.FORBIDDEN_PHRASES:
            if phrase in explanation_text:
                violations.append(f"금지 표현 감지: '{phrase}'")

        passed = len(violations) == 0
        return {
            "agent": "RiskGuardAgent",
            "passed": passed,
            "violations": violations,
            "checks_performed": [
                "개별 종목 추천 없음",
                "수익률 보장 표현 없음",
                "과도한 투자권유 없음",
                "환각·최신성 리스크 점검",
                "설명가능성 확인",
            ],
            "note": "수치는 DB/Tool에서, LLM은 설명만 — 환각 방지 적용",
        }


class ReportAgent:
    """
    ACT — Act (final)
    Assembles all structured agent outputs into a Wealth Report.
    LLM is called only for natural-language summary — numbers come from upstream agents.
    """

    def run(
        self,
        profile: dict,
        spending: dict,
        goal: dict,
        products: dict,
        risk_guard: dict,
        cashflow: dict | None = None,
    ) -> dict:
        mode = profile.get("mode", "early_career")

        if mode == "early_career":
            summary = (
                f"[생애 첫 자산관리 리포트]\n\n"
                f"생애단계: {profile['life_stage']} | 우선순위: {profile['priority']}\n"
                f"이번 달 지출: {spending['monthly_spending']:,}원\n"
                f"과소비 항목: {', '.join(spending['overspend_warnings']) or '없음'}\n\n"
                f"절약 넛지: {goal['range_nudge']}\n"
                f"목표 달성 최단 기간: {goal['best_case_months']}개월\n"
                f"6개월 절약 시 실물 환산: {goal['physical_equivalent_6m']}\n\n"
                f"추천 상품군 1순위: {products['products'][0]['name']} "
                f"({products['products'][0]['note']})\n\n"
                f"Risk Guard: {'✅ 통과' if risk_guard['passed'] else '⚠️ 위반 항목 있음'}\n"
                f"{products['disclaimer']}"
            )
        else:
            cf = cashflow or {}
            summary = (
                f"[은퇴 현금흐름 리포트]\n\n"
                f"생애단계: {profile['life_stage']} | 우선순위: {profile['priority']}\n\n"
                f"예상 월 수입: {cf.get('monthly_income', 0):,}원\n"
                f"예상 월 지출: {cf.get('monthly_expense', 0):,}원\n"
                f"월 부족분: {abs(cf.get('monthly_shortage', 0)):,}원 "
                f"({'부족' if cf.get('monthly_shortage', 0) < 0 else '충분'})\n"
                f"리스크 등급: {cf.get('risk_level', '미산출')}\n\n"
                f"추천 상품군 1순위: {products['products'][0]['name']} "
                f"({products['products'][0]['note']})\n\n"
                f"Risk Guard: {'✅ 통과' if risk_guard['passed'] else '⚠️ 위반 항목 있음'}\n"
                f"{products['disclaimer']}"
            )

        next_actions = (
            [
                "배달앱 주문을 이번 주 2회로 줄이기",
                "전북은행 ISA 계좌 개설 검토",
                "구독 서비스 1개 정리 (월 약 2~3만원)",
            ]
            if mode == "early_career"
            else [
                "연금 부족분 보완 상품 비교 시작",
                "보험료 부담률 점검 (월 소득 대비 10% 기준)",
                "JB금융 은퇴 설계 상담 예약",
            ]
        )

        return {
            "agent": "ReportAgent",
            "report_text": summary,
            "next_actions": next_actions,
            "risk_guard_passed": risk_guard["passed"],
        }


# ─── Orchestrator ─────────────────────────────────────────────────────────────

class WealthPathOrchestrator:
    """
    Multi-Agent Workflow Controller.
    Runs 7 agents sequentially, passing structured outputs between stages.

    Pipeline:
        ProfileAgent → SpendingAgent / CashflowAgent
        → GoalAgent → ProductAgent
        → RiskGuardAgent → ReportAgent
    """

    def __init__(self):
        self.profile_agent   = ProfileAgent()
        self.spending_agent  = SpendingAgent()
        self.cashflow_agent  = CashflowAgent()
        self.goal_agent      = GoalAgent()
        self.product_agent   = ProductAgent()
        self.risk_guard      = RiskGuardAgent()
        self.report_agent    = ReportAgent()

    def run(
        self,
        user: UserProfile,
        transactions: list[Transaction],
        target_amount: int = 10_000_000,
        monthly_medical: int = 250_000,
    ) -> dict:
        log = []

        # ── STAGE 1: JUDGE (Perceive) ─────────────────────────────────────
        profile  = self.profile_agent.run(user)
        log.append({"stage": 1, "agent": "ProfileAgent",  "status": "done"})

        spending = self.spending_agent.run(transactions, user.monthly_income)
        log.append({"stage": 1, "agent": "SpendingAgent", "status": "done"})

        cashflow = self.cashflow_agent.run(user, monthly_medical)
        log.append({"stage": 1, "agent": "CashflowAgent", "status": "done"})

        # ── STAGE 2: ACT ──────────────────────────────────────────────────
        goal     = self.goal_agent.run(user, spending, target_amount)
        log.append({"stage": 2, "agent": "GoalAgent",     "status": "done"})

        products = self.product_agent.run(profile)
        log.append({"stage": 2, "agent": "ProductAgent",  "status": "done"})

        # ── STAGE 3: VERIFY ───────────────────────────────────────────────
        risk     = self.risk_guard.run(
            all_results=[profile, spending, goal, products],
            explanation_text=goal.get("range_nudge", ""),
        )
        log.append({"stage": 3, "agent": "RiskGuardAgent","status": "done"})

        # ── FINAL: Report ─────────────────────────────────────────────────
        report   = self.report_agent.run(profile, spending, goal, products, risk, cashflow)
        log.append({"stage": 3, "agent": "ReportAgent",   "status": "done"})

        return {
            "execution_log":   log,
            "profile":         profile,
            "spending":        spending,
            "cashflow":        cashflow,
            "goal":            goal,
            "products":        products,
            "risk_guard":      risk,
            "report":          report,
        }
